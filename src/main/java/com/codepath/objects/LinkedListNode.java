package main.java.com.codepath.objects;


/**
 * The type LinkedListNode.
 */
public class LinkedListNode {

    /**
     * The Val.
     */
    public int val;

    /**
     * The Next.
     */
    public LinkedListNode next;

    /**
     * Instantiates a new List node.
     *
     * @param x the x
     */
    LinkedListNode(int x) {
        val = x; next = null;
    }
}
